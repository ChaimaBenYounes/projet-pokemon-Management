<?php

namespace App\Entity;

use Doctrine\ORM\Mapping as ORM;


class Pokemon
{
  
    private $id;


    private $nom;

    public function getId(): ?int
    {
        return $this->id;
    }

    public function getNom(): ?string
    {
        return $this->nom;
    }

    public function setNom(string $nom): self
    {
        $this->nom = $nom;

        return $this;
    }
}
